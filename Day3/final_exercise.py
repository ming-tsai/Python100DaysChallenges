left_or_right = input('Where do you want to go? Type "left" or "right": ').lower()

if left_or_right != "left":
    print("You fell into a hole.\nGame Over.")
else:
    swim_or_wait = input('Type "swim" or "wait": ').lower()
    if swim_or_wait != "wait":
        print("You got attacked by trout.\nGame Over.")
    else:
        which_door = input("Which door? Red, Yellow or Blue? ").lower()
        if which_door == "yellow":
            print("You Win! 🏆")
        elif which_door == "red":
            print("It's a room full of fire.\nGame Over.")
        elif which_door == "blue":
            print("Eaten by beasts.\nGame Over.")
        else:
            print(f'There is no "{which_door}" door!\nGame Over.')